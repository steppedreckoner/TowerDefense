	public static void Update(){
		switch(gameState){
		case MAINMENU:
			if (mainMenu == null){
				mainMenu = new MainMenu();
			}
			mainMenu.Update();
			break;
		case GAME:
			if (game == null){
				game = new Game(map);
			}
			game.Update();
			break;
		case EDITOR:
			if (editor == null){
				editor = new Editor();
			}
			editor.Update();
			break;
		}
	}
	
